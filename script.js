// SIDE MENU

function showMenu () {
	document.getElementById("side-menu").style.visibility = "visible";
	document.getElementById("side-menu-icon").style.visibility = "hidden";
	console.log("hi")
}

function hideMenu () {
	document.getElementById("side-menu").style.visibility = "hidden";
	document.getElementById("side-menu-icon").style.visibility = "visible";
}